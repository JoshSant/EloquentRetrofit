package com.example.eloquentretrofit.model.pojo;

public class Ventas {

    private long id;
    private String marca, modelo, matricula, imagen, fecha;

    public Ventas(String marca, String modelo, String matricula, String imagen, String fecha) {
        this.marca = marca;
        this.modelo = modelo;
        this.matricula = matricula;
        this.imagen = imagen;
        this.fecha = fecha;
    }

    public long getId() {
        return id;
    }

    public void setId(long id) {
        this.id = id;
    }

    public String getMarca() {
        return marca;
    }

    public void setMarca(String marca) {
        this.marca = marca;
    }

    public String getModelo() {
        return modelo;
    }

    public void setModelo(String modelo) {
        this.modelo = modelo;
    }

    public String getMatricula() {
        return matricula;
    }

    public void setMatricula(String matricula) {
        this.matricula = matricula;
    }

    public String getImagen() {
        return imagen;
    }

    public void setImagen(String imagen) {
        this.imagen = imagen;
    }

    public String getFecha() {
        return fecha;
    }

    public void setFecha(String fecha) {
        this.fecha = fecha;
    }
}
